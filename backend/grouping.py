# 反向查找：stock_id → group_name
_REVERSE_MAP: dict[str, str] = {}
for _group, _ids in THEME_GROUPS.items():
    for _sid in _ids:
        if _sid not in _REVERSE_MAP:   # 第一個命中的族群優先
            _REVERSE_MAP[_sid] = _group


def assign_theme_group(stock_id: str) -> str:
    """依族群字典分配族群名稱，未命中回傳「其他/未分組」"""
    return _REVERSE_MAP.get(stock_id[:4], "其他/未分組")
